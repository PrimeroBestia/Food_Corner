			<h1> Baked Tonkatsu</h1>
		<p class="country">Eastern Dish (Japan)</p>

			<img src="img/tonkats.jpg">
			<p class="mdtime">Takes 30 minutes</p>
			<p class="mdnum">Makes 2 servings</p>
			<p class="md">MAIN DISH</p><br>
			<p class="description">
				Tonkatsu (pork cutlet), is a Japanese dish which consists of a breaded, deep-fried pork cutlet. There are two main types, fillet and loin. It is often served with shredded cabbage. In Korea, tonkatsu is known as don-gaseu or don-kkaseu, which derived from a transliteration of the Japanese word. Tonkatsu originated in Japan in the 19th century. Early katsuretsu was usually beef; the pork version was invented in Japan in 1899 at a restaurant called Rengatei in Tokyo. It was originally considered a type of yoshoku-Japanese versions of European cuisine invented in the late 19th and early 20th centuries-and was called katsuretsu or simply katsu<br><br>
			</p>

			<div class="ingredients">
				<h3>Ingredients</h3>
				<ul>
					<li> 3/4 cup panko (Japanese breadcrumbs)</li>	
					<li> 1 Tbsp extra virgin olive oil	</li>
					<li> 2 1/2 -thick lean boneless</li>
					<li>&emsp;pork loin chops (2 pieces = 1/2 lb or 227 g, 1/2 = 1.2 cm)</li>
					<li> Freshly ground black pepper</li>	
					<li> 2 Tbsp all-purpose flour</li>
					<li> Kosher salt</li>
					<li> 1 large egg</li>
					<li> 1 Tbsp white and black sesame seeds (roasted/toasted)</li>
					<li> Tonkatsu Sauce</li>
				</ul>
			</div>
			<div class="instruction">
				<h3>Instruction</h3>
				<ul>
					<li><a>1. Gather all the ingredients.</a></li>
					<li><a>2. Adjust an oven rack to the middle position and preheat the oven to 400F (200C). Line a rimmed baking sheet with parchment paper.</a></li>
					<li><a>3. Combine the panko and oil in a frying pan and toast over medium heat until golden brown. Transfer panko into a shallow dish and allow to cool.</a></li>
					<li><a>4. Get rid of the extra fat and make a couple of slits on the connective tissue between the meat and fat. The reason why you do this is that red meat and fat have different elasticity, and when they are cooked they will shrink and expand at different rates. This will allow Tonkatsu to stay nice and flat and prevent Tonkatsu from curling up.</a></li>
					<li><a >5. Pound the meat with a meat pounder, or if you don't have one then just use the back of knife to pound. Mold the extended meat back into original shape with your hands.</a></li>
					<li><a >6. Sprinkle salt and freshly ground black pepper.</a></li>
					<li><a>7. Dredge each pork piece in the flour to coat completely and pat off the excess flour. Then dip into the beaten egg and finally coat with the toasted panko. Press on the panko flakes to make sure they adhere to the pork.</a></li>
					<li><a>8. Place the pork on the prepared baking sheet or even better if you have an oven-safe wire rack (as air goes through on the bottom so panko won't get crushed). Bake until the pork is no longer pink inside, about 20 minutes.</a></li>
					<li><a>9. Place the pork on the prepared baking sheet or even better if you have an oven-safe wire rack (as air goes through on the bottom so panko won't get crushed). Bake until the pork is no longer pink inside, about 20 minutes.</a></li>
					<li><a>10. To make special sesame tonkatsu sauce, grind black and white sesame seeds in a mortor and add tonkatsu sauce. Mix all together.</a></li><br>
				</ul>
			</div>

