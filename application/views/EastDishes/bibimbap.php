		<div class="recipe-container">
			<h1>Bibimbap (Mixed rice with vegtables)</h1>
		<p class="country">Eastern Dish (Korea)</p>

			<img src="img/bibimbap1.jpg">
			<p class="mdtime">Takes 60 minutes</p>
			<p class="mdnum">Makes 4 servings</p>
			<p class="md">MAIN DISH </p><br>
			<p class="description">
				Bibimbap, sometimes anglicized as bi bim bap or bi bim bop, is a Korean dish. The word literally means "mixed rice". Bibimbap is served as a bowl of warm white rice topped with namul (sauteed and seasoned vegetables) and gochujang (chili pepper paste), soy sauce, or doenjang (a fermented soybean paste). A raw or fried egg and sliced meat (usually beef) are common additions. The hot dish is stirred together thoroughly just before eating.<br><br>
			</p>

			<div class="ingredients">
				<h3>Ingredients</h3>
				<ul>
    				<li> 5 cups cooked short-grain rice</li>
    				<li> 12 ounces soy bean sprouts, washed and drained</li>
    				<li> 8 ounces of spinach blanched and washed with the excess water squeezed out by hand</li>
    				<li> 1 large carrot, red bell pepper and zucchini</li>
    				<li> 1 English cucumber</li>
    				<li> 3 to 4 green onions, chopped</li>
    				<li> 4 ounces fernbrake (gosari), fresh or soaked from 1/2 ounce dried gosari (details below)</li>
    				<li> 1 ounce dried bellflower roots (doraji), soaked in cold water for 18 to 24 hours.</li>
    				<li> soy sauce</li>
    				<li> 4 eggs</li>
    				<li> salt</li>
    				<li> vegtable oil</li>
    				<li> sesame oil</li>
    				<li> garlic</li>
    				<li> honey or sugar</li>
    				<li> sesame oil</li>
    				<li> Gochujang (korean hot pepper paste)</li>
    			</ul>
			</div>
			<div class="instruction">
				<h3>Instruction</h3>
				<ul>
					<li><a>1. HOW TO PREPARE DRIES FERNBRAKE (GOSARI) FOR USE: </a></li>
						<li><a>&emsp;->If you have presoaked or fresh fernbrake you can use it straight away, but if you have dried fernbrake you'll need to get it ready to eat. It's fast if you have a pressure cooker, but if you don't it will take some time.</a></li>
					<li><a>2. RICE: </a></li>
						<li><a>&emsp;->Rinse 2 cups of rice in cold water and scrub the wet rice with your hand. Rinse and drain until the drained water is pretty clear.</a></li>
						<li><a>&emsp;->Put the rice in a heavy-bottomed pot. Add 2 cups of water, cover, and soak for 30 minutes.</a></li>
						<li><a>&emsp;->Cook over medium high heat for 7 to 8 minutes until the surface is covered with abundant bubbles that are spluttering noisily and look like they're about to overflow the pot. Turn the rice over a few times with a spoon and cover the pot again.</a></li>
						<li><a>&emsp;->Turn the heat to very low and simmer for another 10 minutes until the rice is fully cooked and fluffy. Remove from the heat.</a></li>
						<li><a>&emsp;->Fluff the rice with a spoon to release excess steam. Let the rice stand, covered, at room temperature to keep it warm</a></li>
					<li><a>3. SOYBEAN SPROUTS: </a></li>
						<li><a>&emsp;->Put the soy bean sprouts in a pot and add 4 cups water and 2 or 3 teaspoons salt. Cover and cook for 20 minutes over medium high heat. Take out the sprouts with tongs and put them into a bowl, leaving about 1/2 cup of sprouts in the pot with the water you used to boil them. This is the soup to serve with bibimbap later.</a></li>
						<li><a>&emsp;->In a bowl, mix the sprouts by hand with 1/2 teaspoons salt, 1 teaspoon minced garlic, and 2 teaspoons sesame oil. Put them on the large platter.</a></li>
					<li><a>4. SPINACH: </a></li>
						<li><a>&emsp;->Clean the mushrooms with damp towel and slice them.Crack 3 eggs in a bowl and add 1/4 teaspoon salt. Beat it with fork and remove the stringy chalaza.Cut up the blanched spinach a few times and put it in a bowl. Mix by hand with 1 teaspoon garlic, 1 teaspoon sesame oil, 1/2 teaspoon salt, and 1 teaspoon sesame seeds. Cover and put it next to the soy bean sprouts on the platter.</a></li>
					<li><a>5. OTHER FRESH VEGTABLES: </a></li>
						<li><a>&emsp;->Cut the carrot into matchsticks, put them in a bowl, and mix with a pinch of salt. Let stand for 5 to 10 minutes until sweating.</a></li>
						<li><a>&emsp;->Cut the red bell pepper into halves, deseed, and slice into strips. Put them in a bowl.</a></li>
						<li><a>&emsp;->Cut the zucchini into matchsticks and mix with 1/2 teaspoon salt.</a></li>
						<li><a>&emsp;->Cut the cucumber into halves lengthwise and slice thinly crosswise. Mix with 1/4 teaspoon salt.</a></li>
					<li><a>6. BEEF: </a></li>
						<li><a>&emsp;->Cut the beef into matchsticks and put them in a bowl. Then mix with 1 tablespoon minced garlic, 1 tablespoon soy sauce, 1 tablespoon honey, 2 teaspoons sesame oil, and 1 teaspoon sesame seeds with a spoon. Cover and keep in the fridge until ready to use.</a></li>
					<li><a> 7. MOUNTAIN VEGTABLES:</a></li>
						<li><a>&emsp;->Cut the fernbrake (gosari) a few times into bite size pieces. Set aside. Put the bellflower roots (doraji) in a large bowl. Add 1 or 2 tablespoons salt. Rub for a minute to wilt slightly and release some of the bitterness. Rinse them in cold water a couple of times and drain. If you find some roots are too thick, split them lengthwise. Set aside.<a></li><br>
					<li><a> LET'S COOK!</a></li><br>
						<li><a>&emsp;->Heat up a pan over medium high heat. Squeeze out excess water from the carrot. Add a few drops of cooking oil to the pan and saute the carrot for 1 minute. Put it on the platter next to the soy bean sprouts and spinach. Clean the pan with wet paper towel or wash it.</a></li>
						<li><a>&emsp;->Heat a few drops of cooking oil in the pan and squeeze out the excess water from the cucumber. Saute with 1/2 teaspoon minced garlic and a few drops of sesame oil for 30 seconds. Put it on the platter. Clean the pan.</a></li>
						<li><a>&emsp;->Heat up the pan with a few drops of cooking oil. Add the red bell pepper and sprinkle a pinch of salt over top. Saute for 30 seconds. Put it on the platter. Clean the pan.</a></li>
						<li><a>&emsp;->Heat up the pan and squeeze out excess water from the zucchini. Add a few drops of cooking oil and saute with 1 teaspoon minced garlic, 1 tablespoon chopped green onion, a drop of sesame oil for 1 minute until slightly softened. Put it on the platter. Clean the pan.</a></li>
						<li><a>&emsp;->Heat up the pan with a few drops of cooking oil. Add the bellflower roots and saute for 2 to 3 minutes. Lower the heat to medium so as not to brown them. Add 1 teaspoon minced garlic and a drop of sesame oil. Stir for another minute until a little softened. Put it on the platter. Clean the pan.</a></li>
						<li><a>&emsp;->Put some sesame oil on the finshed rolls and sprinkle some sesame seeds over top. Cut each roll into 1/4 inch bite size pieces with a sharp knife, occasionally wiping it with a wet paper towel or cloth to clean the starch off and to ease cutting.</a></li>
						<li><a>&emsp;->Heat up the pan. Add a few drops of cooking oil. Stir the gosari for 2 minutes until a little softened. Add 1/2 teaspoon of minced garlic, 2 teaspoons soy sauce, and 2 teaspoons sugar, and keep stirring for another minute. Put it on the platter.</a></li><br>
				</ul>
