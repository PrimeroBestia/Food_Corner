			<h1>Lo Mein</h1>
		<p class="country">Eastern Dish (China)</p>

			<img src="img/lomein1.jpg">
			<p class="appetime">Takes 25 minutes</p>
			<p class="appenum">Makes 4 servings</p>
			<p class="appe">APPETIZER </p><br>
			<p class="description">
				Lo mein is a Chinese dish with wheat flour noodles. It often contains vegetables and some type of meat or seafood, usually beef, chicken, pork, shrimp or wontons. It can also be eaten with just vegetables.The term lo mein comes from the Cantonese lou1 min6, meaning "stirred noodles". The Cantonese usage of the character pronounced lou1 and meaning "to stir", differs from the character's usual meaning of "to dredge" or "to scoop out of water" in Mandarin, in which case it would be pronounced as laau4 or lou4 in Cantonese (lao in Mandarin). In Mandarin, the dish is more typically called lao mian. In its country of origin, it is made of thin flour and egg noodles which are notable for their elastic texture.<br><br>
			</p>

			<div class="ingredients">
				<h3>Ingredients</h3>
				<ul>
    				<li> 8 ounces lo mein egg noodles</li>
    				<li> 1 tablespoon olive oil </li>
    				<li> 2 cloves garlic, minced</li>
    				<li> 2 cups cremini mushrooms, sliced</li>
    				<li> 1 red bell pepper, julienned</li>
    				<li> 1 carrot, julienned</li>
    				<li> 1/2 cup snow peas</li>
    				<li> 3 cups baby spinach</li>
    				<li> 1 tsp. sesame oil</li>
    				<li> 2 tablespoons reduced sodium soy sauce, or more, to taste</li>
    				<li> 2 teaspoons sugar</li>
    				<li> 1/2 teaspoon ground ginger</li>
    				<li> 1/2 teaspoon Sriracha, or more, to taste</li>
    			</ul>
			</div>
			<div class="instruction">
				<h3>Instructions</h3>
				<ul>
					<li><a>1. In a small bowl, whisk together soy sauce, sugar, sesame oil, ginger and Sriracha; set aside.</a></li>
					<li><a>2.In a large pot of boiling water, cook noodles according to package instructions; drain well.</a></li>
					<li><a>3.Heat olive oil in a large skillet or wok over medium high heat. Add garlic, mushrooms, bell pepper and carrot. Cook, stirring frequently, until tender, about 3-4 minutes. Stir in snow peas and spinach until the spinach has wilted, about 2-3 minutes.<a></li>
					<li><a>4. Stir in egg noodles and soy sauce mixture, and gently toss to combine. Then serve immediately.</a></li><br>
				</ul>
