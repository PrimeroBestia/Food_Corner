			<h1>Keema Beef Curry</h1>
		<p class="country">Eastern Dish (India)</p>

			<img src="img/kbc1.jpg">
			<p class="sdtime">Takes 45 minutes</p>
			<p class="sdnum">Makes 4-6 servings</p>
			<p class="sd">SIDE DISH</p><br>
			<p class="description">
				Keema or Qeema is a traditional meat dish from the South Asia. The word may be borrowed from the Turkish kiyma, which means minced meat. It is typically minced beef or mutton curry (lamb or goat) with peas or potatoes. Keema can be made from almost any meat, can be cooked by stewing or frying, and can be formed into kebabs. Keema is also sometimes used as a filling for samosas or naan.<br><br>
			</p>

			<div class="ingredients">
				<h3>Ingredients</h3>
				<ul>
    				<li> 1 tbsp. canola oil </li>
    				<li> 2 tbsp. minced fresh ginger</li>
    				<li> 2 clove garlic</li>
    				<li> 1 can unsweetened coconut milk</li>
    				<li> 1 1/2 c. frozen baby peas </li>
    				<li> 1 1/2 tbsp. madras curry powder </li>
    				<li> 1 large Yukon gold potato</li>
    				<li> 1 1/2 lb. lean ground sirloin </li>
    				<li> salt and pepper to taste </li>
    				<li> 1 large onion </li>
    				<li> 1 can diced tomatoes with their juices </li>
    				<li> Chopped cilantro, warm naan, and steamed rice</li>
    			</ul>
			</div>
			<div class="instruction">
				<h3>Instructions</h3>
				<ul>
					<li><a>1. In a large, deep skillet, heat the oil. Add the ground meat and cook over high heat, stirring to break up the lumps, until no longer pink, about 5 minutes.</a></li>
					<li><a>2. Add the onion, ginger, garlic, and curry powder and season with salt and pepper. Cook over high heat, stirring frequently, until the onion is softened, about 3 minutes.</a></li>
					<li><a>3. Add the potato, broth, coconut milk, and the tomatoes and their juices and bring to a boil. Cook over moderate heat, stirring occasionally, until the potato is tender, about 15 minutes.<a></li>
					<li><a>4. Using the back of a spoon, lightly crush some of the potato. Add the peas and cook just until heated through. Serve in bowls with cilantro, naan, and rice.</a></li><br>
				</ul>
