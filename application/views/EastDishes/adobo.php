		<div class="recipe-container">
			<h1>Chicken Adobo</h1>
		<p class="country">Eastern Dish (Philippines)</p>

			<img src="img/adobo1.jpg">
			<p class="mdtime">Takes 80 minutes</p>
			<p class="mdnum">Makes 4 serving</p>
			<p class="md">MAIN DISH </p><br>
			<p class="description">
				Philippine Adobo (from Spanish adobar: "marinade," "sauce" or "seasoning") is a popular dish and cooking process in Philippine cuisine that involves meat, seafood, or vegetables marinated in vinegar, soy sauce, garlic, and black peppercorns, which is browned in oil, and simmered in the marinade. It has sometimes been considered as the unofficial national dish in the Philippines.<br><br>
			</p>

			<div class="ingredients">
				<h3>Ingredients</h3>
				<ul>
    				<li> 1/2 cup to 3/4 cup of rice vinegar (adjust it to your taste)</li>
    				<li> 1 lime</li>
    				<li> 1 tbsp water</li>
    				<li> 1 tbsp corn starch</li>
    				<li> 1/2 cup/125 ml water</li>
    				<li> 1/2 tsp fresh ground black pepper</li>
    				<li> 2-3 bay leaves</li>
    				<li> 1 potato, medium, cut in large chunks</li>
    				<li> 1 carrot, medium, cut in large chunks</li>
    				<li> 5 garlic cloves, crushed</li>
    				<li> 2 long piece of ginger, grated</li>
    				<li> 2 tbsp vegetable oil</li>
					<li> 8 chicken pieces, legs and thighs</li>	
					<li> 1/2 cup/125 ml soy sauce/Bragg or Kikkoman brand for gluten-free version</li>
				</ul>
			</div>
			<div class="instruction">
				<h3>Instructions</h3>
				<ul>
					<li><a>1. In a large bowl combine soy sauce, rice vinegar, 1 tbsp of oil, 4 garlic cloves crushed, half of grated ginger, black pepper, add chicken pieces and let them marinade for 30 minutes in the fridge.</a></li>
					<li><a>2. In a wok or a deep pan brown chicken pieces for 3 minutes on each side, might need to do that in batches not to overcrowd chicken, otherwise they will steam instead of brown. Do not discard the marinade. Chicken will not be cooked all the way through, remove it from the pan to a plate and set aside.</a></li>
					<li><a>3. To the same pan add 1 tbsp of vegetable oil, sliced garlic and the remaining ginger, stir fry for 1 minute, do not let them brown. Add the chicken, marinade, water, bay leaves, potatoes and carrots, (the sauce will not cover the chicken entirely), turn the heat up and bring the sauce to a boil, then reduce the heat to low and let it simmer for 45 minutes until carrots and potatoes are easily pierced with a fork and the chicken is cooked all the way through.</a></li>
					<li><a>4. In a small bowl mix cornstarch with water, making sure there are no clumps and add to the sauce, mix through and let it simmer for a couple more minutes, which will allow the sauce to thicken. Spoon the sauce over the chicken, it should be thick enough to coat the pieces. If the sauce is not thick enough, cook it a bit longer until desired consistency. Sprinkle with sliced spring onion.</a></li>
					<li><a>5. Serve with plain rice and lime wedges.</a></li><br>
				</ul>
