			<h1>Wonton Soup </h1>
		<p class="country">Eastern Dish (China)</p>

			<img src="img/wonton1.jpg">
			<p class="appetime">Takes 30 minutes</p>
			<p class="appenum">Makes 4 servings</p>
			<p class="appe">APPETIZER </p><br>
			<p class="description">
				Wonton soup! These wontons are filled with a juicy pork and prawn / shrimp filling and will knock your socks off. There are some couple of cheeky tips and when you see, you are going to be so surprised how fast and EASY these are to make.The first time you have a real wonton, whether homemade or at a proper restaurant that makes them fresh with real meat or seafood fillings (rather than fillers), you will be blown away. The filling is tender and you can actually taste what it's made of, rather than being a small grey ball of meat made of who-knows-what.<br><br>
			</p>

			<div class="ingredients">
				<h3>Ingredients</h3>
				<ul>
    				<li> 2/3 lb. ground pork</li>
    				<li> 4 tsp. soy sauce (2 tsp. for the soup and another 2 tsp for the wontons </li>
    				<li> 2 tsp. Chives, thinly sliced</li>
    				<li> 1 tsp. rice wine vinegar</li>
    				<li> 1 tsp. cornstarch</li>
    				<li> 1 tsp. grated ginger</li>
    				<li> 3 garlic cloves (1 clove for the wontons and 2 smashed cloves for the soup)</li>
    				<li> 1/2 tsp. crushed red pepper flakes</li>
    				<li> 1/2 tsp. sesame oil</li>
    				<li> 1 package square wonton wrappers</li>
    				<li> 1/4 c. water</li>
    				<li> 4 c. chicken stock</li>
    				<li> 2" piece of peeled ginger</li>
    				<li> 1/4 tsp. sesame oil</li>
    				<li> 2 tbsp. Sliced green onions, for garnish</li>
    			</ul>
			</div>
			<div class="instruction">
				<h3>Instructions</h3>
				<ul>
					<li><a>1. In a large bowl, mix pork, soy sauce, chives, vinegar, cornstarch, ginger, garlic, red pepper flakes and sesame oil until fully incorporated.</a></li>
					<li><a>2. Using your finger, wet the edges of wonton wrapper with water. Place half a tablespoon of pork filling in the center of the wonton wrapper. Fold wonton in half diagonally to create a triangle, and seal the edges. Fold the two identical corners in on each other and press again to seal. Repeat until all wonton wrappers are filled.</a></li>
					<li><a>3.Bring all soup ingredients to a boil. Simmer on low for 10 minutes, then remove ginger and garlic cloves and bring it back to a boil. Lower in wontons and cook for 10 minutes more. Serve into bowls and garnish with green onions.<a></li><br>
				</ul>
