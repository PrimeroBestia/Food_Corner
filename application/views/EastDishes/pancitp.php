			<p class="appetime">Takes 60 minutes</p>
			<p class="appenum">Makes 12 servings</p>
			<p class="appe">APPETIZER</p><br>
			<p class="description">
				In Filipino cuisine, pancit are noodles. Noodles were introduced into the Philippines early on by Chinese Filipino settlers in the archipelago, and over the centuries have been fully adopted into local cuisine, of which there are now numerous variants and types. The term pancit is derived from the Hokkien pian i sit which literally means "convenient food.<br><br>
