			<h1>Gyudon (Beef Bowl)</h1>
		<p class="country">Eastern Dish (Japan)</p>

			<img src="img/gyudon1.jpg">
			<p class="mdtime">Takes 20 minutes</p>
			<p class="mdnum">Makes 2 servings</p>
			<p class="md">MAIN DISH</p><br>
			<p class="description">
				Gyudon (beef bowl), is a Japanese dish consisting of a bowl of rice topped with beef and onion simmered in a mildly sweet sauce flavored with dashi (fish and seaweed stock), soy sauce and mirin (sweet rice wine). It also often includes shirataki noodles, and is sometimes topped with a raw egg or a soft poached egg (onsen tamago). A very popular food in Japan, it is commonly served with beni shoga (pickled ginger), shichimi (ground chili pepper), and a side dish of miso soup. Gyu is the prefix for anything cow-related, and don is short for donburi, the Japanese word for "bowl".<br><br>
			</p>

			<div class="ingredients">
				<h3>Ingredients</h3>
				<ul>
					<li> 1 onion</li>	
					<li> 2 green onions/scallions</li>
					<li> 3/4 lb thinly sliced beef (chuck or rib eye) (3/4 lb = 12 oz or 340 g</li>
					<li> 1 Tbsp neutral flavor oil (vegetable, canola, etc)</li>
					<li> 2 Tbsp sake</li>	
					<li> 2 Tbsp mirin</li>
					<li> 1 Tbsp soy sauce</li>
					<li> 3 large eggs (beaten, optional)/li>
					<li> 3 cups cooked Japanese short grain rice</li>
					<li> Pickled red ginger (to garnish)</li>
				</ul>
			</div>
			<div class="instruction">
				<h3>Instruction</h3>
				<ul>
					<li><a>1. Gather all the ingredients.</a></li>
					<li><a>2. Cut the onion and green onion into thin slices and cut the meat into small pieces.</a></li>
					<li><a>3. Heat the oil in a large frying pan over medium high heat and cook the onions until tender.</a></li>
					<li><a>4. Add the meat and sugar and cook until no longer pink.</a></li>
					<li><a >5. Add sake, mirin, and soy sauce.</a></li>
					<li><a >6. Reduce the heat and simmer for 2-3 minutes.</a></li>
					<li><a>7. If you like to add the egg, slowly and evenly drizzle the beaten egg over the beef. Cook covered until the egg is done. Add the green onion right before removing from the heat.</a></li>
					<li><a>8. Place the beef and egg on top of steamed rice and pour desired amount of sauce. Top with pickled red ginger.</a></li><br>
				</ul>
