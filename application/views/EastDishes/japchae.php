			<h1>Japchae<br>(Sweet potato starch noodles stir fried<br>with vegetables)</h1>
		<p class="country">Eastern Dish (Korea)</p>

			<img src="img/japchae1.jpg">
			<p class="sdtime">Takes 40 minutes</p>
			<p class="sdnum">Makes 4 servings</p>
			<p class="sd">SIDE DISH</p><br>
			<p class="description">
				Japchae is a sweet and savory dish of stir-fried glass noodles and vegetables that is popular in Korean cuisine. Japchae is typically prepared with dangmyeon, a type of cellophane noodles made from sweet potato starch; the noodles are topped with assorted vegetables, meat, and mushrooms, and seasoned with soy sauce and sesame oil.Once a royal dish, it is now one of the most popular traditional celebration dishes, often served on special occasions, such as weddings, birthdays (especially dol, the first birthday, and hwangap, the sixtieth), and holidays.Japchae is commonly served as a banchan (side dish), though it may also be a main dish. It is sometimes served on a bed of rice: with rice, it is known as japchae-bap<br><br>
			</p>

			<div class="ingredients">
				<h3>Ingredients</h3>
				<ul>
    				<li> 4 ounces beef, filet mignon (or pork shoulder), cut into 1/2 inch wide and 2 1/2inch long strips</li>
    				<li> 2 large dried shiitake mushrooms, soaked in warm water for 2 to 3 hours, cut into thin strips</li>
    				<li> 2 garlic cloves, minced</li>
    				<li> 2 tablespoons plus 1 teaspoon soy sauce</li>
    				<li> 1 tablespoons plus 2 teaspoons sugar</li>
    				<li> 2 tablespoons sesame oil</li>
    				<li> 1 tablespoon toasted sesame seeds</li>
    				<li> 1 large egg</li>
    				<li> 4 ounces spinach, washed and drained</li>
    				<li> 4 ounces of dangmyeon (sweet potato starch noodles)</li>
    				<li> 2 to 3 green onions, cut crosswise into 2 inch long pieces</li>
    				<li> 1 medium onion (1 cup), sliced thinly</li>
    				<li> 4 to 5 white mushrooms, sliced thinly</li>
    				<li> 1 medium carrot (3/4 cup), cut into matchsticks</li>
    				<li> ground black pepper</li>
    				<li> vegtable oil</li>
    				<li> salt</li>
    			</ul>
			</div>
			<div class="instruction">
				<h3>Instruction</h3>
				<ul>
					<li><a>1. MARINATE THE BEEF AND MUSHROOM:</a></li>
						<li><a>&emsp;->Put the beef and shiitake mushrooms into a bowl and mix with 1 clove of minced garlic, 1 teaspoon sugar, 1/4 teaspoon ground black pepper, 2 teaspoons soy sauce, and 1 teaspoon of sesame oil with a wooden spoon or by hand. Cover and keep it in the fridge.</a></li>
					<li><a>2. MAKE THE EGG GRANISH (JIDAN): </a></li>
						<li><a>&emsp;->Crack the egg and separate the egg yolk from the egg white. Remove the white stringy stuff (chalaza) from the yolk. Beat in a pinch of salt with a fork.</a></li>
						<li><a>&emsp;->Crack the egg and separate the egg yolk from the egg white. Remove the white stringy stuff (chalaza) from the yolk. Beat in a pinch of salt with a fork.</a></li>
						<li><a>&emsp;->To keep the jidan as yellow as possible, turn off the heat and pour the egg yolk mixture into the pan. Tilt it around so the mixture spreads thinly. Let it cook using the remaining heat in the pan for about 1 minute. Flip it over and let it sit on the pan for 1 more minute.</a></li>
						<li><a>&emsp;->Let it cool and slice it into thin strips.</a></li>
					<li><a>3. PREPARE THE NOODLES AND VEGTABLES: </a></li>
						<li><a>&emsp;->Bring a large pot of water to a boil. Add the spinach and blanch for 30 seconds to 1 minute, then take it out with a slotted spoon or strainer. Let the water keep boiling to cook the noodles. Rinse the spinach in cold water to stop it from cooking. Squeeze it with your hands to remove any excess water. Cut it a few times and put it into a bowl. Mix with 1 teaspoon soy sauce and 1 teaspoon sesame oil. Put it into a large mixing bowl.</a></li>
						<li><a>&emsp;->Put the noodles into the boiling water, cover and cook for 1 minute. Stir them with a wooden spoon so they don't stick together. Cover and keep cooking for another 7 minutes until the noodles are soft and chewy.</a></li>
						<li><a>&emsp;->Put the noodles into the boiling water, cover and cook for 1 minute. Stir them with a wooden spoon so they don't stick together. Cover and keep cooking for another 7 minutes until the noodles are soft and chewy.</a></li>
						<li><a>&emsp;->Strain and cut them a few times with kitchen scissors. Put the noodles into the large bowl next to the spinach. Add 2 teaspoons sesame oil, 1 teaspoon soy sauce, and 1 teaspoon sugar. Mix well by hand or a wooden spoon. This process will season the noodles and also keep the noodles from sticking to each other.</a></li>
						<li><a>&emsp;->Heat up a skillet over medium high heat. Add 2 teaspoons vegetable oil with the onion, the green onion, and a pinch of salt. Stir-fry about 2 minutes until the onion looks a little translucent. Transfer to the noodle bowl. Heat up the skillet again and add 2 teaspoons vegetable oil. Add the white mushrooms and a pinch of salt. Stir-fry for 2 minutes until softened and a little juicy. Transfer to the noodle bowl.</a></li>
						<li><a>&emsp;->Heat up the skillet and add 1 teaspoon vegetable oil. Add the carrot and stir-fry for 20 seconds. Add the red bell pepper strips and stir-fry another 20 seconds. Transfer to the noodle bowl.</a></li>
						<li><a>&emsp;->Heat up the skillet and add 2 teaspoons vegetable oil. Add the beef and mushroom mixture and stir fry for a few minutes until the beef is no longer pink and the mushrooms are softened and shiny. Transfer to the noodle bowl.</a></li>
					<li><a>6. MIX AND SERVE: </a></li>
						<li><a>&emsp;->Add 1 minced garlic clove, 1 tablespoon soy sauce, 1 tablespoon sugar, 1/2 teaspoon ground black pepper, and 2 teaspoons of sesame oil to the mixing bowl full of ingredients. Mix all together by hand.</a></li>
						<li><a>&emsp;->Add the egg garnish and 1 tablespoon sesame seeds. Mix it and transfer it to a large plate and serve.<a></li><br>
				</ul>
