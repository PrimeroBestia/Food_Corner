			<p class="description">
				Orange chicken is a Chinese dish of Hunan origin. The variety of orange chicken most commonly found at North American Chinese restaurants consists of chopped, battered, and fried chicken pieces coated in a sweet orange-flavored chili sauce, which thickens or caramelizes to a glaze. While the dish is very popular in the United States, it is most often found as a variation of General Tso's chicken in North America rather than the dish found in mainland China. Chef Andy Kao claims to have developed the original Chinese-American orange chicken recipe at a Panda Express in Hawaii in 1987. Since Panda Express is closely associated with this dish, Panda Express uses orange chicken as a promotion tool by having a dedicated food truck tour the country distributing samples of orange chicken.<br><br>
