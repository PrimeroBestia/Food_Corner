					<li><a>1. Place chicken pieces in a gallon size resealable bag, set aside. Place chicken pieces in a gallon size resealable bag, set aside. Whisk mixture well, then measure out 2/3 cup of the mixture and pour it over the chicken in resealable bag. Whisk mixture well, then measure out 2/3 cup of the mixture and pour it over the chicken in resealable bag. Place bag in a bowl or baking dish and refrigerate 30 minutes.</a></li>
