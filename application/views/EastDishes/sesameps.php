			<h1>Sesame Chicken<br>Potstickers</h1>
		<p class="country">Eastern Dish (China)</p>

			<img src="img/scps1.jpg">
			<p class="sdtime">Takes 30 minutes</p>
			<p class="sdnum">Makes 36 pieces</p>
			<p class="sd">SIDE DISH</p><br>
			<p class="description">
				Potstickers is a kind of Chinese dumpling, commonly eaten in China and other parts of East Asia. They are one of the major foods eaten during the Chinese New Year and year-round in the northern provinces. Though considered part of Chinese cuisine and popular in other parts of Asia and in Western countries. Potstickers typically consist of a ground meat and/or vegetable filling wrapped into a thinly rolled piece of dough, which is then sealed by pressing the edges together.<br><br>
			</p>

			<div class="ingredients">
				<h3>Ingredients</h3>
				<ul>
    				<li> 1 pound ground chicken</li>
    				<li> 3 ounces shiitake mushrooms, diced</li>
    				<li> 2 cloves garlic, pressed</li>
    				<li> 2 green onions, thinly sliced</li>
    				<li> 2 tablespoons reduced sodium soy sauce</li>
    				<li> 1 tablespoon freshly grated ginger</li>
    				<li> 1 teaspoon rice vinegar</li>
    				<li> 1/4 teaspoon white pepper</li>
    				<li> 1 tsp. sesame oil</li>
    				<li> 36 won ton wrappers</li>
    				<li> 2 tablespoons vegetable oil</li>
    				<li> Soy sauce, for serving</li>
    			</ul>
			</div>
			<div class="instruction">
				<h3>Instructions</h3>
				<ul>
					<li><a>1. In a large bowl, combine chicken, mushrooms, garlic, green onions, soy sauce, sesame oil, ginger, rice vinegar and white pepper.</a></li>
					<li><a>2. To assemble the dumplings, place wrappers on a work surface. Spoon 1 tablespoon of the chicken mixture into the center of each wrapper. Using your finger, rub the edges of the wrappers with water. Fold the dough over the filling to create a half-moon shape, pinching the edges to seal.</a></li>
					<li><a>3. Heat vegetable oil in a large skillet over medium heat. Add potstickers in a single layer and cook until golden and crisp, about 2-3 minutes per side.<a></li>
					<li><a>4. Serve immediately with soy sauce, if desired.</a></li><br>
				</ul>
			</div>

