			<h1>Gimbap (Seaweed Rice Rolls)</h1>
		<p class="country">Eastern Dish (Korea)</p>

			<img src="img/gimbap1.jpg">
			<p class="sdtime">Takes 45 minutes</p>
			<p class="sdnum">Makes 2-5 servings</p>
			<p class="sd">SIDE DISH </p><br>
			<p class="description">
				Gimbap is a Korean dish made from cooked rice and other ingredients that are rolled in gim—dried sheets of laver seaweed—and served in bite-sized slices. The dish is often part of a packed meal, or dosirak, to be eaten at picnics and outdoor events, and can serve as a light lunch along with danmuji (yellow pickled radish) and kimchi. It is a popular take-out food in Korea and abroad, and is known as a convenient food because of its portability.<br><br>
			</p>

			<div class="ingredients">
				<h3>Ingredients</h3>
				<ul>
    				<li> 5 sheets of gim (seaweed paper), roasted slightly</li>
    				<li> 4 cups cooked rice (the recipe is here, but make with 2 cups of short grain rice instead of 1 cup)</li>
    				<li> 1/2 pound beef skirt steak (or tenderloin, or ground beef)</li>
    				<li> 1 large carrot, cut into matchsticks (about 1 1/2 cup)</li>
    				<li> 5 strips of yellow pickled radish (use pre-cut danmuji or cut into 8 inch long strips)</li>
    				<li> 8  to 10 ounces spinach (1 small bunch), blanched, rinsed in cold water, and strained</li>
    				<li> 3 eggs</li>
    				<li> 3 garlic cloves</li>
    				<li> 2 tbs soy sauce</li>
    				<li> 1 tablespoon plus 1 teaspoon brown (or white) suga</li>
    				<li> 1 1/2 teaspoon salt</li>
    				<li> 2 1/2 tablespoons sesame oil</li>
    				<li> vegetable oil</li>
    			</ul>
			</div>
			<div class="instruction">
				<h3>Instruction</h3>
				<ul>
					<li><a>1. RICE: </a></li>
						<li><a>&emsp;->Clean the mushrooms with damp towel and slice them.Place freshly made rice in a large, shallow bowl. Gently mix in 1/2 teaspoon salt and 2 teaspoons sesame oil over top with a rice scoop or a wooden spoon.</a></li>
						<li><a>&emsp;->Let it cool down enough so it's no longer steaming. Cover and set aside.</a></li>
					<li><a>2. SPINACH: </a></li>
						<li><a>&emsp;->Combine the blanched spinach, 2 minced garlic cloves, 1/2 teaspoon salt, and 2 teaspoons sesame oil in a bowl.</a></li>
						<li><a>&emsp;->Mix well by hand and put it on a large platter with the sliced yellow pickled radish.</a></li>
					<li><a>3. CARROTS: </a></li>
						<li><a>&emsp;->Combine the carrot matchsticks with 1/4 teaspoon salt. Mix well and let it sweat for 5 to 10 minutes. Heat a pan and add a few drops vegetable oil.</a></li>
						<li><a>&emsp;->Squeeze out excess water from the carrot, then saute for about 1 minute. Put it on the platter next to the spinach.</a></li>
					<li><a>4. STEAKS: </a></li>
						<li><a>&emsp;->Clean the mushrooms with damp towel and slice them.Trim the fat from the skirt steaks and slice into 1/4 inch wide, 3 to 5 inch strips. Put the strips into a bowl. Add 2 teaspoons soy sauce, 1 minced garlic clove, 1/4 teaspoon ground black pepper,1 tablespoon plus 1 teaspoon brown (or white) sugar, and 2 teaspoons sesame oil.</a></li>
						<li><a>&emsp;->Mix well by hand. Set aside, and let them marinate while we do the egg strips</a></li>
					<li><a>5. EGGS: </a></li>
						<li><a>&emsp;->Clean the mushrooms with damp towel and slice them.Crack 3 eggs in a bowl and add 1/4 teaspoon salt. Beat it with fork and remove the stringy chalaza.</a></li>
						<li><a>&emsp;->Drizzle a few drops of oil on a heated 10 to 12 inch non-stick pan. Wipe off the excess with a paper towel so only a thin sheen of oil remains. Turn down the heat to low and pour the egg mixture into the pan. Spread it into a large circle so it fills the pan.</a></li>
						<li><a>&emsp;->When the bottom of the egg is cooked, flip it over with a spatula. Remove from the heat and let it cook slowly in the hot pan for about 5 minutes, with the ultimate goal of keeping the egg as yellow as possible, and not brown.</a></li>
						<li><a>&emsp;->When the bottom of the egg is cooked, flip it over with a spatula. Remove from the heat and let it cook slowly in the hot pan for about 5 minutes, with the ultimate goal of keeping the egg as yellow as possible, and not brown.</a></li>
					<li><a>6. LET'S ROLL GIMBAP: </a></li>
						<li><a>&emsp;->Clean the mushrooms with damp towel and slice them.When the bottom of the egg is cooked, flip it over with a spatula. Remove from the heat and let it cook slowly in the hot pan for about 5 minutes, with the ultimate goal of keeping the egg as yellow as possible, and not brown.</a></li>
						<li><a>&emsp;->Place beef, carrot, yellow pickled radish strip, a few egg strips, and spinach in the center of the rice.a></li>
						<li><a>&emsp;->Cut the ham into small pieces.Use both hands to roll the mat (along with gim and rice) over the fillings, so one edge of the rice and gim reaches the opposite edge. This centers the fillings in the roll, so they'll be nicely in the middle when you slice it.</a></li>
